/* -------------------------------------------------------------------------
 * petmate_screencode_from_grid()
 * ------------------------------------------------------------------------- */
int petmate_screencode_from_grid(const int *order, int row, int col)
{
    if (row < 0 || row >= PETMATE_GRID_ROWS || col < 0 || col >= PETMATE_GRID_COLS)
        return -1;
    return order[row * PETMATE_GRID_COLS + col];
}

/* -------------------------------------------------------------------------
 * petmate_grid_from_screencode()
 * ------------------------------------------------------------------------- */
int petmate_grid_from_screencode(const int *order, int screencode,
                                 int *out_row, int *out_col)
{
    int i;
    for (i = 0; i < PETMATE_CHARORDER_SIZE; i++) {
        if (order[i] == screencode) {
            *out_row = i >> 4;   /* i / 16 */
            *out_col = i & 15;   /* i % 16 */
            return 1;
        }
    }
    return 0;
}

/* -------------------------------------------------------------------------
 * petmate_petscii_to_screencode()
 * Converts a raw PETSCII byte from a SEQ/PRG file to a C64 screencode.
 * Source: src/utils/importers/seq2petscii.ts (default case in chrout())
 * ------------------------------------------------------------------------- */
int petmate_petscii_to_screencode(unsigned int c)
{
    if (c == 0xff)
        return 94;
    if (c >= 0x20 && c <= 0x3f)
        return (int)c;
    if (c >= 0x40 && c <= 0x5f)
        return (int)(c - 0x40);
    if (c >= 0x60 && c <= 0x7f)
        return (int)(c - 0x20);
    if (c >= 0xa0 && c <= 0xbf)
        return (int)(c - 0x40);
    if (c >= 0xc0 && c <= 0xfe)
        return (int)(c - 0x80);
    return -1; /* control code or unmapped */
}
